package operator;

public class Operator4_1 {
    public static void main(String[] args) {
        int a = 10, b = 4;

        System.out.println("a + b = " + (a + b));

        System.out.println("a - b = " + (a - b));

        System.out.println("a * b = " + (a*b));

        System.out.println("a / b = " + (a/b));

        System.out.println("a / (float)b = " + (a/(float)b));
    }
}
